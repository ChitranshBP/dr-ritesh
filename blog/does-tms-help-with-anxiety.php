<script>
(function () {
    var body       = document.getElementById('postBody');
    var progress   = document.getElementById('postProgress');
    var headings   = Array.from(body.querySelectorAll('h2[id]'));
    var tocList    = document.getElementById('tocList');
    var drawerList = document.getElementById('tocDrawerList');

    if (headings.length < 2) {
        document.querySelector('.post-toc-sidebar') && (document.querySelector('.post-toc-sidebar').style.display = 'none');
        document.querySelector('.post-toc-mobile')  && (document.querySelector('.post-toc-mobile').style.display  = 'none');
        return;
    }

    // Build TOC lists
    headings.forEach(function (h) {
        var label = h.textContent.replace(/[?!.]$/, '').trim();
        var li  = document.createElement('li');
        li.innerHTML = '<a class="post-toc-link" href="#' + h.id + '"><span class="post-toc-dot"></span>' + label + '</a>';
        tocList.appendChild(li);

        var dLi = document.createElement('li');
        dLi.innerHTML = '<a href="#' + h.id + '">' + label + '</a>';
        drawerList.appendChild(dLi);
    });

    // Scroll spy
    var tocLinks    = tocList.querySelectorAll('.post-toc-link');
    var drawerLinks = drawerList.querySelectorAll('a');
    var observer = new IntersectionObserver(function (entries) {
        entries.forEach(function (e) {
            if (e.isIntersecting) {
                var id = e.target.id;
                tocLinks.forEach(function (l) { l.classList.toggle('active', l.getAttribute('href') === '#' + id); });
                drawerLinks.forEach(function (l) { l.classList.toggle('active', l.getAttribute('href') === '#' + id); });
            }
        });
    }, { rootMargin: '-10% 0px -75% 0px', threshold: 0 });
    headings.forEach(function (h) { observer.observe(h); });

    // Reading progress bar
    window.addEventListener('scroll', function () {
        var rect   = body.getBoundingClientRect();
        var total  = body.offsetHeight;
        var done   = Math.max(0, -rect.top + window.innerHeight * 0.7);
        progress.style.width = Math.min(100, (done / total) * 100) + '%';
    }, { passive: true });

    // Mobile drawer toggle
    var toggle = document.getElementById('tocToggle');
    var drawer = document.getElementById('tocDrawerList');
    toggle && toggle.addEventListener('click', function () {
        var open = drawer.classList.toggle('open');
        toggle.classList.toggle('open', open);
        toggle.setAttribute('aria-expanded', open);
    });

    // FAQ accordion
    document.querySelectorAll('.post-faq-trigger').forEach(function (btn) {
        btn.addEventListener('click', function () {
            var body  = btn.nextElementSibling;
            var icon  = btn.querySelector('.post-faq-icon');
            var isOpen = btn.getAttribute('aria-expanded') === 'true';
            btn.setAttribute('aria-expanded', !isOpen);
            if (!isOpen) {
                icon.style.transform = 'rotate(180deg)';
                body.style.maxHeight = body.scrollHeight + 'px';
            } else {
                icon.style.transform = '';
                body.style.maxHeight = '0';
            }
        });
    });
})();
</script>

